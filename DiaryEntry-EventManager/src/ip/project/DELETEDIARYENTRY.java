    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
   
        int result = JOptionPane.showConfirmDialog(null,"Sure? You want to DELETE?", "DELETION",
               JOptionPane.YES_NO_OPTION,
               JOptionPane.QUESTION_MESSAGE);
        if(result == JOptionPane.YES_OPTION)
         try {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/ipproject1","root","123456");
            Statement stmt=con.createStatement();
            DateFormat df=new SimpleDateFormat("yyyy-MM-dd");  
            String date1 = df.format(date123.getDate());
            
            String query = "DELETE FROM diaryentry1 WHERE date="+"\""+date1+"\""+";";
            int n=stmt.executeUpdate(query);
           
            stmt.close();
            con.close();
             if(n==0)
                  JOptionPane.showMessageDialog(null,"NO ENTRY FOUND!");
                 else
           JOptionPane.showMessageDialog(null,"DELETION SUCCESSFULL!");
        } catch (Exception ex) {
            Logger.getLogger(DELETEDIARYENTRY.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

