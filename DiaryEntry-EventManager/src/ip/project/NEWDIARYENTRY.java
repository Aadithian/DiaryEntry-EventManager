    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
        DateFormat df=new SimpleDateFormat("yyyy-MM-dd");  
            
            if(entry.getText().equals(""))
            JOptionPane.showMessageDialog(null,"ENTER FIELD EMPTY ");
              else if(place.getText().equals(""))
            JOptionPane.showMessageDialog(null,"PLACE FIELD EMPTY ");
              else if(time123.getText().equals(""))
            JOptionPane.showMessageDialog(null,"TIME FIELD EMPTY ");
              else if(date123.getDate().equals(""))
            JOptionPane.showMessageDialog(null,"DATE NOT SPECIFIED");
             
              else{
        try {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/ipproject1","root","123456");
            Statement stmt=con.createStatement();
            String entry1=entry.getText();
            String place1=place.getText();
            String time=time123.getText();
            String date1 = df.format(date123.getDate());
            String z = null;
            if(happyrdb.isSelected()==true)
                z="Happy";
            else if(avgrdb.isSelected()==true)
                z="Average";
            else if(sadrdb.isSelected()==true)
                z="Sad";
            String query = "Insert into diaryentry1 values("+"\""+entry1+"\""+","+"\""+place1+"\""+","+"\""+date1+"\""+","+"\""+time+"\""+","+"\""+z+"\""+");";
            
            stmt.executeUpdate(query);
            stmt.close();
            con.close();
          JOptionPane.showMessageDialog(null,"ENTRY ADDED!");
                entry.setText("");
           place.setText("");
           time123.setText("");
           happyrdb.setSelected(false);
           avgrdb.setSelected(false);
           sadrdb.setSelected(false);
            
            
        } catch (Exception ex) {
                JOptionPane.showMessageDialog(this,"Unsupported Character(s) inserted! or other logical error!");
                
        }   }  
            
            
    }//GEN-LAST:event_enterActionPerformed
                                                             
