    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
            DateFormat df=new SimpleDateFormat("yyyy-MM-dd");  
         if(event.getText().equals(""))
            JOptionPane.showMessageDialog(null,"ENTER FIELD EMPTY ");
          else if(place.getText().equals(""))
            JOptionPane.showMessageDialog(null,"PLACE NOT SPECIFIED");
           else if(date123.getDate().equals(""))
            JOptionPane.showMessageDialog(null,"DATE NOT SPECIFIED");
          else if(date123.getDate().equals(""))
            JOptionPane.showMessageDialog(null,"DATE NOT SPECIFIED");
           else if(starttime.getText().equals(""))
            JOptionPane.showMessageDialog(null," START TIME FIELD EMPTY ");
           else if(endtime.getText().equals(""))
            JOptionPane.showMessageDialog(null," END TIME FIELD EMPTY ");
        
        else
        try {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/ipproject1","root","123456");
            Statement stmt=con.createStatement();

            String date1 = df.format(date123.getDate());
            String event1=event.getText();
            String place1=place.getText();
            String starttime1=starttime.getText();
             String endtime1=endtime.getText();
            String a = null;
            if(flagrdb.isSelected()==true)
                 a="Yes";
            else
                a="No";
            String query = "Insert into eventmanager values("+"\""+event1+"\""+","+"\""+date1+"\""+","+"\""+place1+"\""+","+"\""+starttime1+"\""+","+"\""+endtime1+"\""+","+"\""+a+"\""+");";
            stmt.executeUpdate(query);
            stmt.close();
            con.close();
            JOptionPane.showMessageDialog(null,"ENTRY ADDED!");
             event.setText("");
           place.setText("");
           starttime.setText("");
           endtime.setText("");
           flagrdb.setSelected(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,"UNSUPPORTED CHARACTER FORMAT OR UNIDENTIFIED LOGICAL ERROR!");
        }
        
    }//GEN-LAST:event_addActionPerformed

